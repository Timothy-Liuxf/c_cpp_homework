#pragma warning(disable : 4996)
#include <stdio.h>
#include <math.h>
double sabc(double a, double b, double c)
{
	double p;
	p = a / 2 + b / 2 + c / 2;
	return sqrt(p * (p - a) * (p - b) * (p - c));
}
int main()
{
	double p, a, b, c, s;
	printf("Please input a , b , c.\n");
	scanf("%lf%lf%lf", &a, &b, &c);
	s = sabc(a, b, c);
	printf("%lf", s);
	return 0;
}